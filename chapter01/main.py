'''



산술 연산자
1. + : 더하기
2. - : 빼기
3. * : 곱하기
4. / : 나누기
5. % : 나머지
5 까지는 다른 프로그래밍 언어에서도 사용되는데
6. // : 몫
7. ** : 제곱
'''
print(2**4) # 2^4
#나누기 연산자/ 몫 연산자의 결과값 차이
print(3/2)          # 결과값 : 1.5
print(3//2)         # 결과값 : 1
print(type(3/2))    # 결과값: <class 'float'>
print(type(3//2))   # 결과값: <class 'int'>

